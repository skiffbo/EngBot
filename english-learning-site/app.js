function getTodayRoute() {
  const now = new Date();
  const day = now.getDay();
  const weekday = new Intl.DateTimeFormat("zh-CN", {
    year: "numeric",
    month: "long",
    day: "numeric",
    weekday: "long"
  }).format(now);

  if ([1, 3, 5].includes(day)) {
    return { weekday, sourceKey: "bbc", note: "今天按规则进入 BBC 新闻阅读。" };
  }

  if ([2, 4, 6].includes(day)) {
    return { weekday, sourceKey: "zhongkao", note: "今天按规则进入上海中考阅读题练习。" };
  }

  return {
    weekday,
    sourceKey: "mixed",
    note: "今天是周日，原规则未指定来源；已进入混合复习日。"
  };
}

function pickLesson(sourceKey) {
  const pool =
    sourceKey === "mixed" ? lessons : lessons.filter((lesson) => lesson.sourceKey === sourceKey);
  return pool[Math.floor(Math.random() * pool.length)];
}

async function fetchRemoteLesson(forceRefresh = false) {
  const params = new URLSearchParams();
  if (forceRefresh) {
    params.set("force", "1");
    params.set("variant", String(Date.now()));
  }
  const url = `/daily-lesson.json${params.toString() ? `?${params.toString()}` : ""}`;
  const response = await fetch(url, {
    headers: {
      Accept: "application/json"
    }
  });

  if (!response.ok) {
    throw new Error(`Daily lesson API failed: ${response.status}`);
  }

  return response.json();
}

function normalizeLesson(rawLesson) {
  return {
    ...rawLesson,
    sourceKey: rawLesson.sourceKey || rawLesson.route?.sourceKey || "remote",
    sourceLabel: rawLesson.sourceLabel || rawLesson.route?.sourceLabel || "联网阅读材料",
    sourceName: rawLesson.sourceName || rawLesson.provider || "Live source",
    sourceUrl: rawLesson.sourceUrl || "#",
    sentenceTranslations: rawLesson.sentenceTranslations || [],
    grammar: Array.isArray(rawLesson.grammar) ? rawLesson.grammar : []
  };
}

function normalizeWord(word) {
  return word
    .toLowerCase()
    .trim()
    .replace(/^[^a-z]+|[^a-z]+$/g, "");
}

function getWordsFromInput() {
  const words = els.wordInput.value
    .split(/[\s,，;；、]+/)
    .map(normalizeWord)
    .filter(Boolean);
  return [...new Set(words)];
}

function countWords(text) {
  return text.split(/\s+/).filter(Boolean).length;
}

function speak(text) {
  if (!("speechSynthesis" in window)) return;
  window.speechSynthesis.cancel();
  const utterance = new SpeechSynthesisUtterance(text);
  utterance.lang = "en-US";
  utterance.rate = 0.86;
  window.speechSynthesis.speak(utterance);
}

function updateSteps(maxIndex) {
  [...els.stepList.children].forEach((item, index) => {
    item.classList.toggle("done", index < maxIndex);
    item.classList.toggle("active", index === maxIndex);
  });
}

function readRecords() {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY)) || [];
  } catch {
    return [];
  }
}

function writeRecords(records) {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(records));
  } catch {
    // Local storage can be unavailable in strict browser modes; the lesson still works.
  }
}

function renderRecords() {
  const records = readRecords();

  if (!records.length) {
    els.recordSummary.textContent = "还没有保存记录。";
    return;
  }

  const latest = records[0];
  els.recordSummary.innerHTML = `
    已保存 <strong>${records.length}</strong> 次练习。<br>
    最近：${latest.date}<br>
    篇目：${latest.title}<br>
    生词：${latest.words.length ? latest.words.join(", ") : "未填写"}
  `;
}

function saveProgress(stage, extra = {}) {
  if (!state.lesson) return;

  const date = new Intl.DateTimeFormat("zh-CN", {
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit"
  }).format(new Date());
  const key = `${new Date().toISOString().slice(0, 10)}-${state.lesson.id}`;
  const records = readRecords();
  const nextRecord = {
    key,
    date,
    stage,
    title: state.lesson.title,
    source: state.lesson.sourceLabel,
    words: [...state.enteredWords],
    challengeWord: state.challengeWord,
    copied: els.copyCheck.checked,
    readAloud: els.readCheck.checked,
    ...extra
  };
  const existingIndex = records.findIndex((record) => record.key === key);

  if (existingIndex >= 0) {
    records.splice(existingIndex, 1);
  }

  writeRecords([nextRecord, ...records].slice(0, 30));
  renderRecords();
}

async function renderLesson(options = {}) {
  const route = getTodayRoute();
  const forceRefresh = Boolean(options.forceRefresh);
  state.remoteStatus = "loading";
  els.weekdayLabel.textContent = route.weekday;
  els.sourceLabel.textContent = "联网抓取中";
  els.routeNote.textContent = "正在请求后端 /daily-lesson.json，抓取并解析今日材料。";
  els.articleTitle.textContent = "正在生成今日阅读";
  els.sourceLink.href = "#";
  els.sourceLink.textContent = "Live API";
  els.articleText.textContent = "请稍等，后端正在联网抓取来源、解析正文并生成今日学习材料。";
  els.wordCount.textContent = "loading";

  try {
    state.lesson = normalizeLesson(await fetchRemoteLesson(forceRefresh));
    state.remoteStatus = "online";
  } catch (error) {
    console.warn(error);
    state.lesson = pickLesson(route.sourceKey);
    state.remoteStatus = "fallback";
  }

  state.enteredWords = [];
  state.challengeWord = "";

  els.sourceLabel.textContent = state.lesson.sourceLabel;
  els.routeNote.textContent =
    state.remoteStatus === "online"
      ? `${state.lesson.route?.note || route.note} 已通过后端联网抓取与解析，并生成学习改写版。`
      : `${route.note} 后端暂不可用，当前显示本地兜底材料。`;
  els.articleTitle.textContent = state.lesson.title;
  els.sourceLink.href = state.lesson.sourceUrl;
  els.sourceLink.textContent = state.lesson.sourceName;
  els.articleText.textContent = state.lesson.paragraph;
  els.wordCount.textContent = `${state.lesson.wordCount || countWords(state.lesson.paragraph)} words`;
  els.wordInput.value = "";
  els.sentenceInput.value = "";
  els.studyContent.classList.add("hidden");
  els.copyCheck.checked = false;
  els.readCheck.checked = false;
  els.sentenceFeedback.classList.remove("show");
  els.sentenceFeedback.innerHTML = "";
  updateCopyStatus();
  updateSteps(0);
  renderRecords();
}

function getVocabEntry(word) {
  return (
    vocab[word] || {
      ipa: "/请查词典/",
      pos: "word",
      meaning: "這個詞尚未收入本頁詞庫。請先根據上下文猜意思，再查權威詞典確認。",
      examples: [
        `I found the word "${word}" in today's reading passage.`,
        `I will write "${word}" in my vocabulary notebook.`
      ]
    }
  );
}

function renderVocabCards() {
  els.vocabCards.innerHTML = "";

  state.enteredWords.forEach((word) => {
    const entry = getVocabEntry(word);
    const card = document.createElement("article");
    card.className = "vocab-card";
    card.innerHTML = `
      <header>
        <div>
          <h4>${word}</h4>
          <span class="ipa">${entry.ipa}</span>
        </div>
        <span class="tag">${entry.pos}</span>
      </header>
      <p>${entry.meaning}</p>
      <ol class="examples">
        ${entry.examples
          .map(
            (example) => `
              <li>
                ${example}
                <button class="speak-small" type="button" aria-label="朗读例句">▶</button>
              </li>`
          )
          .join("")}
      </ol>
    `;

    [...card.querySelectorAll(".speak-small")].forEach((button, index) => {
      button.addEventListener("click", () => speak(entry.examples[index]));
    });

    els.vocabCards.appendChild(card);
  });
}

function renderGrammar() {
  els.grammarList.innerHTML = "";

  state.lesson.grammar.forEach((item, index) => {
    const card = document.createElement("article");
    card.className = "grammar-card";
    card.innerHTML = `
      <span class="formula">${index + 1}. ${item.formula}</span>
      <blockquote>${item.sentence}</blockquote>
      <p>${item.explanation}</p>
    `;
    els.grammarList.appendChild(card);
  });
}

function renderSentencePairs() {
  els.sentencePairs.innerHTML = "";

  state.lesson.grammar.forEach((item, index) => {
    const pair = document.createElement("article");
    pair.className = "sentence-pair";
    pair.innerHTML = `
      <span>${index + 1}</span>
      <div>
        <p class="pair-en">${item.sentence}</p>
        <p class="pair-zh">${state.lesson.sentenceTranslations[index] || ""}</p>
      </div>
    `;
    els.sentencePairs.appendChild(pair);
  });
}

function chooseChallengeWord() {
  if (!state.enteredWords.length) {
    state.challengeWord = "";
    els.challengeWord.textContent = "-";
    return;
  }
  state.challengeWord = state.enteredWords[Math.floor(Math.random() * state.enteredWords.length)];
  els.challengeWord.textContent = state.challengeWord;
}

function buildStudyContent() {
  const words = getWordsFromInput();

  if (!words.length) {
    els.wordInput.focus();
    els.wordInput.placeholder = "请先输入至少一个生词，例如：context, confident";
    return;
  }

  state.enteredWords = words;
  renderVocabCards();
  renderGrammar();
  renderSentencePairs();
  chooseChallengeWord();
  els.englishCopy.textContent = state.lesson.paragraph;
  els.chineseTranslation.textContent = state.lesson.translation;
  els.studyContent.classList.remove("hidden");
  updateSteps(2);
  saveProgress("已生成学习内容");
}

function updateCopyStatus() {
  if (els.copyCheck.checked && els.readCheck.checked) {
    els.copyStatus.textContent = "很好，现在可以完成三句造句练习。";
    updateSteps(6);
    if (state.enteredWords.length) saveProgress("已完成抄写与朗读");
  } else {
    els.copyStatus.textContent = "完成两项后，再进入造句练习。";
    if (state.enteredWords.length) saveProgress("学习进行中");
  }
}

function splitStudentSentences(text) {
  return text
    .split(/\n+/)
    .map((line) => line.trim())
    .filter(Boolean);
}

function sentenceContainsWord(sentence, word) {
  const escaped = word.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return new RegExp(`\\b${escaped}\\b`, "i").test(sentence);
}

function detectIssues(sentence, word, index) {
  const issues = [];

  if (!sentenceContainsWord(sentence, word)) {
    issues.push(`第 ${index + 1} 句没有使用抽到的生词 "${word}"。`);
  }

  if (!/^[A-Z]/.test(sentence)) {
    issues.push(`第 ${index + 1} 句句首建议大写。`);
  }

  if (!/[.!?]$/.test(sentence)) {
    issues.push(`第 ${index + 1} 句结尾建议加 . / ? / !。`);
  }

  const checks = [
    {
      pattern: /\bI\s+is\b/i,
      message: `第 ${index + 1} 句中 "I is" 应改为 "I am"。`
    },
    {
      pattern: /\b(you|we|they)\s+is\b/i,
      message: `第 ${index + 1} 句中 you/we/they 后面不要用 is，可按意思改用 are。`
    },
    {
      pattern: /\b(he|she|it)\s+are\b/i,
      message: `第 ${index + 1} 句中 he/she/it 后面不要用 are，可按意思改用 is。`
    },
    {
      pattern: /\b(he|she|it)\s+(go|have|do|make|need|say|read)\b/i,
      message: `第 ${index + 1} 句如果是一般现在时，he/she/it 后的实义动词通常要加 -s 或使用特殊形式。`
    },
    {
      pattern: /\ba\s+[aeiou]/i,
      message: `第 ${index + 1} 句里 a 后接元音音素开头的词时，通常要改成 an。`
    },
    {
      pattern: /\ban\s+[bcdfghjklmnpqrstvwxyz]/i,
      message: `第 ${index + 1} 句里 an 后如果不是元音音素开头，通常要改成 a。`
    },
    {
      pattern: /\b(am|is|are)\s+go\b/i,
      message: `第 ${index + 1} 句里 be + go 应按意思改成 be going 或直接用 go/goes。`
    }
  ];

  checks.forEach((check) => {
    if (check.pattern.test(sentence)) issues.push(check.message);
  });

  return issues;
}

function getModelSentences(word) {
  const entry = getVocabEntry(word);
  return entry.examples.length >= 2
    ? [
        entry.examples[0],
        entry.examples[1],
        `I can use "${word}" clearly when I write about today's article.`
      ]
    : [
        `I found "${word}" in today's reading passage.`,
        `The word "${word}" is useful for this topic.`,
        `I can make a clear sentence with "${word}".`
      ];
}

function checkSentences() {
  if (!state.challengeWord) {
    els.sentenceFeedback.classList.add("show");
    els.sentenceFeedback.innerHTML = "<h4>还没有抽到生词</h4><p>请先输入生词并生成学习内容。</p>";
    return;
  }

  const sentences = splitStudentSentences(els.sentenceInput.value);
  const issues = [];

  if (sentences.length < 3) {
    issues.push("请至少输入三句，每句单独一行。");
  }

  sentences.slice(0, 3).forEach((sentence, index) => {
    issues.push(...detectIssues(sentence, state.challengeWord, index));
  });

  const models = getModelSentences(state.challengeWord);

  els.sentenceFeedback.classList.add("show");
  els.sentenceFeedback.innerHTML = `
    <h4>${issues.length ? "需要订正的地方" : "基础检查通过"}</h4>
    ${
      issues.length
        ? `<ul>${issues.map((issue) => `<li>${issue}</li>`).join("")}</ul>`
        : "<p>三句都包含目标生词，并通过了基础大小写、标点和常见语法检查。</p>"
    }
    <h4>可参考的正确句子</h4>
    <ol>${models.map((sentence) => `<li>${sentence}</li>`).join("")}</ol>
  `;
  updateSteps(7);
  saveProgress("已检查造句", { sentenceCount: sentences.length, issueCount: issues.length });
}

